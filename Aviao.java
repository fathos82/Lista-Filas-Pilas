public class Aviao {
    private String name;
    private int indetificador;

    public Aviao(String name, int indetificador) {
        this.name = name;
        this.indetificador = indetificador;
    }


    public String getName() {
        return this.name;
    }

    public int getIndetificador() {
        return this.indetificador;
    }


    
}
